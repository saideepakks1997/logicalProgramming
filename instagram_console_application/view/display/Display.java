package display;

public class Display {
	public void displayError(String error) {
		System.out.println("--------------------");
		System.out.println(error);
		System.out.println("--------------------");
	}

	public void displaySuccess(String success) {
		System.out.println("--------------------");
		System.out.println(success);
		System.out.println("--------------------");
	}
}
